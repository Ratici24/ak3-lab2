/*
 * lab1.S - реалізація функції за варіантом 4
 * Лабораторна робота №2
 * Виконав: Миркін Р.І., група ІО-31, залікова № 3114
 *
 * Варіант обирається за номером залікової: 3114 % 5 = 4.
 *
 * Функція варіанту 4 (кускова):
 *
 *              ┌─ (a | b) · c,  якщо (a · b) ≥ 10
 *   f(a,b,c) = │
 *              └─ (a · b) · c,  якщо (a · b) <  10
 *
 *   де "|" — побітове АБО, "·" — множення.
 *
 * Обрані тестові значення:  a = 4, b = 3, c = 5.
 *   - a · b  = 4 · 3 = 12  →  гілка "≥ 10"
 *   - a | b  = 4 | 3 = 7   (100b | 011b = 111b)
 *   - f      = 7 · 5 = 35  =  0x23
 *
 * Очікуваний результат у регістрі r0 після виходу з lab1:  r0 = 35 (0x23).
 */

    .syntax unified
    .global lab1

/* --- Тестові вхідні значення для варіанту --- */
#define A   #4      /* перший аргумент функції */
#define B   #3      /* другий аргумент функції */
#define C   #5      /* третій аргумент функції */

/* =====================================================================
 *  Процедура lab1
 *     Прототип:  uint32_t lab1(void)
 *     Повертає у r0 значення функції за варіантом 4.
 *
 *  Використання регістрів:
 *     r0  – значення a (потім – результат функції)
 *     r1  – значення b
 *     r2  – значення c
 *     r3  – проміжний результат (a · b або a | b)
 *     lr  – адреса повернення (зберігаємо у стеку)
 * ===================================================================== */
    .type lab1, %function
lab1:
    push    {lr}                /* зберігаємо адресу повернення у стек */

    /* --- Завантажуємо вхідні значення у регістри --- */
    mov     r0, A               /* r0 = a */
    mov     r1, B               /* r1 = b */
    mov     r2, C               /* r2 = c */

    /* --- Обчислюємо добуток (a · b) для перевірки умови --- */
    mul     r3, r0, r1          /* r3 = a · b */

    /* --- Перевіряємо умову: (a · b) ≥ 10 ? --- */
    cmp     r3, #10             /* порівнюємо r3 з константою 10 */

    /*
     * IT-блок (If-Then-Else): задаємо умовне виконання двох наступних
     * інструкцій. ITE GE означає: перша інструкція виконується якщо GE,
     * друга — якщо LT (Less Than, протилежна умова).
     */
    ite     ge
    orrge   r3, r0, r1          /* (THEN, GE) r3 = a | b — побітове АБО */
    /* (для гілки LT r3 вже містить добуток a · b, додаткова інструкція не потрібна,
     * але для наочної симетрії залишаємо явний "no-op" mov у стилі ELSE) */
    movlt   r3, r3              /* (ELSE, LT) залишити r3 = a · b як є */

    /* --- Множимо обране проміжне значення на c --- */
    mul     r0, r3, r2          /* r0 = r3 · c  ←  результат функції */

    pop     {lr}                /* відновлюємо lr зі стеку */
    bx      lr                  /* повертаємось до викликача (start.S) */

    .size lab1, .-lab1
