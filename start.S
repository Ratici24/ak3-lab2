/*
 * start.S - стартовий файл для STM32F407VG (Cortex-M4)
 * Лабораторна робота №2
 * Виконав: Миркін Р.І., група ІО-31
 *
 * Призначення:
 *  - Таблиця векторів виключень (vector table) для STM32F4.
 *  - Обробник Reset (__hard_reset__), який викликає процедуру lab1
 *    з обчисленням функції за варіантом, після чого входить у
 *    нескінченний цикл для зручності відлагодження в gdb.
 */

    .syntax unified         /* Уніфікований синтаксис ARM/Thumb */
    .cpu cortex-m4          /* Ядро STM32F407VG */
    //.fpu softvfp          /* FPU не використовуємо */
    .thumb                  /* Cortex-M4 виконує лише Thumb-інструкції */

    /* Експортуємо символи, які потрібні лінкеру */
    .global vtable
    .global __hard_reset__

/*
 * Таблиця векторів виключень.
 * Перший елемент — початкове значення SP (адреса вершини стеку),
 * другий — адреса Reset Handler з обов'язковим бітом thumb-режиму (+1).
 */
    .type vtable, %object
    .type __hard_reset__, %function
vtable:
    .word __stack_start          /* [0]: початкове SP */
    .word __hard_reset__ + 1     /* [1]: адреса Reset Handler у Thumb-режимі */
    .size vtable, .-vtable

/*
 * Reset Handler — точка входу після перезавантаження мікроконтролера.
 * Стек ініціалізується апаратно з vector table, тому додатковій
 * ініціалізації SP вручну не потрібна. Викликаємо процедуру lab1,
 * яка обчислює функцію за варіантом, після чого зациклюємось.
 */
__hard_reset__:
    // SP ініціалізовано апаратно з vtable[0] = __stack_start

    bl    lab1                   /* виклик процедури обчислення функції */

_loop:
    b     _loop                  /* нескінченний цикл для відлагодження */

    .size __hard_reset__, .-__hard_reset__
